#include "libft.h"

int			ft_strequ(char const *s1, char const *s2)
{
	int		equ;

	if (s1 && s2)
	{
		equ = (ft_strcmp(s1, s2) == 0 ? 1 : 0);
		return (equ);
	}
	return (0);
}
